    private void Nombrar_productos(File Productos_file) {
        String[] nombres = controlador.Nombrar_productos(Productos_file);
        producto_1_text.setText(nombres[0]);
        producto_2_text.setText(nombres[1]);
        producto_3_text.setText(nombres[2]);
        bitacora.Escribir(this.getName(), "Se asigno nombre a la lista de los productos correctamente");
    }

    private void Nombrar_Registros(File Carpeta) {
        String[] registros_directorio = controlador.Registros(Carpeta);
        registro_1_text.setText(registros_directorio[0]);
        registro_2_text.setText(registros_directorio[1]);
        registro_3_text.setText(registros_directorio[2]);
        bitacora.Escribir(this.getName(), "Se asigno el directorio de su archivo txt de cada producto");
        
    }

    private void habilitar_confirmar() {
        bitacora.Escribir(this.getName(), "Se comprueba si se puede habilitar el boton confirmar");
        if(!productos_vacios() && !registros_vacios()){
            bitacora.Escribir(this.getName(), "Se habilita el boton confirmar");
            B_confirmar.setEnabled(rootPaneCheckingEnabled);
        }
    }

    private boolean productos_vacios() {
      return (producto_1_text.getText().equals("")) && !(producto_2_text.getText().equals("")) && !(producto_3_text.getText().equals(""));
    }

    private boolean registros_vacios() {
        return (registro_1_text.getText().equals("")) && !(registro_2_text.getText().equals("")) && !(registro_3_text.getText().equals(""));
    }

    public Manejador_productos obtener_productos() { // Devuelve datos a la ventana Votaciones
        bitacora.Escribir(this.getName(), "Se devuelve el listado de productos nuevo");
        return listado;
    }
