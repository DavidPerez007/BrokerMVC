    private void B_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_buscarActionPerformed
        // Proceso de busqueda de un archivo
        bitacora.Escribir(this.getName(), "Se abre buscador de archivo");
        JFileChooser buscador = new JFileChooser();
        int respuesta = buscador.showOpenDialog(this);
        if(respuesta == JFileChooser.APPROVE_OPTION){
            bitacora.Escribir(this.getName(), "Se selecciono un archivo");
            File productos_txt = buscador.getSelectedFile();
            directorio_text.setText(productos_txt.getAbsolutePath());
            Nombrar_productos(productos_txt);
        }else{
            bitacora.Escribir(this.getName(), "Falla buscador de archivo");
            directorio_text.setText("El archivo seleccionado no puede utilizarce");
        }
        habilitar_confirmar();
    }//GEN-LAST:event_B_buscarActionPerformed

