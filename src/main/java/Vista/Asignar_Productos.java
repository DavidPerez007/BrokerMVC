    private void B_buscarCarpetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_buscarCarpetaActionPerformed
        //Processo de busqueda de un directorio/carpeta
        bitacora.Escribir(this.getName(), "Se abre buscador de carpeta");
        JFileChooser buscador = new JFileChooser();
        buscador.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int respuesta = buscador.showOpenDialog(this);
        if(respuesta == JFileChooser.APPROVE_OPTION){
            bitacora.Escribir(this.getName(), "Se sleecciono una carpeta");
            File carpeta_registros = buscador.getSelectedFile();
            directorio_Carpeta.setText(carpeta_registros.getAbsolutePath());
            Nombrar_Registros(carpeta_registros);
        }else{
            bitacora.Escribir(this.getName(), "Falla en la busqueda de carpeta");
            directorio_Carpeta.setText("El archivo seleccionado no puede utilizarce");
        }
        habilitar_confirmar();
    }//GEN-LAST:event_B_buscarCarpetaActionPerformed

